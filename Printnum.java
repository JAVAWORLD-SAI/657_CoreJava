import java.util.*;
class Printnum 
{
	public static void main(String[] args) 
	{
		java.util.Scanner sc= new java.util.Scanner(System.in);
		System.out.println("Enter the Number");
		int n= sc.nextInt();
		for(int i=0; i<n;i++)
		{
			System.out.println(i);
		}
		System.out.println("Hello World!");
	}
}
